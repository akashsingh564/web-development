						<b>Start and re-start the match</b><br>
						1.	A coin is tossed : <br>
									 The team which wins the toss has the choice of whichgoal to attack in the first half of the match or to start the match with a centre pass<br>
									  If the team winning the toss chooses which goal to attack in the first half of the match, the opposing team starts the match<br>
									   If the team winning the toss chooses to start the match, the opposing team has the choice of which goal to attack in the first half of the match. Direction of play is reversed in the second half of the match. <br><br>
						<b>A centre pass is taken : </b><br>
						To start the match by a player from the team winning the toss if they chose this option ; otherwise by a player from the opposing team<br>
						To re-start the match after half-time by a player of the team which did not take the centre pass to start the match<br>
						After a goal by a player of the team against which the goal was scored. <br><br>
