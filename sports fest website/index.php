						4.	substitutions are not permitted for suspended players during their suspension<br>
						5.	after completing a suspension, a player is permitted to be substituted without first returning to the field<br>
						6.	field players must leave or enter the field for substitution purposes within 3 metres of the centre-line on a side of the field agreed with the umpires<br>
